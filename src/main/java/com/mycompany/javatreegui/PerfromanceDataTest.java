public class PerfromanceDataTest {
    @Test
    public void testConstructor() {
        PerformanceData data = new PerformanceData(10, 2);
        assertNotNull(data);
        assertEquals(0, data.rotations);
        assertFalse(data.success);
        assertEquals(10, data.preSize);
        assertEquals(2, data.preHeight);
    }

    @Test
    public void testUpdateTime() {
        PerformanceData data = new PerformanceData(5, 3);
        data.updateTime();
        assertTrue(data.opTime >= 0);
    }
}
